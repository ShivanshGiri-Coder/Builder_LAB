-- AI Features Database Schema

-- AI Resume Templates
CREATE TABLE IF NOT EXISTS ai_resume_templates (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  description TEXT NOT NULL,
  category TEXT NOT NULL CHECK (category IN ('ats_friendly', 'creative', 'executive', 'technical', 'academic')),
  template_structure JSONB NOT NULL DEFAULT '{}'::jsonb,
  sections JSONB NOT NULL DEFAULT '["header", "summary", "experience", "projects", "skills", "education"]'::jsonb,
  styling JSONB NOT NULL DEFAULT '{}'::jsonb,
  is_premium BOOLEAN DEFAULT FALSE,
  usage_count INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- User Resume Generations
CREATE TABLE IF NOT EXISTS user_resume_generations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  template_id BIGINT REFERENCES ai_resume_templates(id) ON DELETE SET NULL,
  resume_content JSONB NOT NULL DEFAULT '{}'::jsonb,
  ai_optimizations JSONB DEFAULT '{}'::jsonb,
  target_job_title TEXT,
  target_industry TEXT,
  keywords TEXT[],
  ats_score DECIMAL(3,2) DEFAULT 0.00,
  export_format TEXT DEFAULT 'pdf' CHECK (export_format IN ('pdf', 'docx', 'txt')),
  is_public BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- AI Interview Sessions
CREATE TABLE IF NOT EXISTS ai_interview_sessions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  session_type TEXT NOT NULL CHECK (session_type IN ('technical', 'behavioral', 'mixed', 'case_study')),
  difficulty_level TEXT NOT NULL CHECK (difficulty_level IN ('junior', 'mid', 'senior', 'lead')),
  target_role TEXT,
  industry_focus TEXT,
  questions JSONB NOT NULL DEFAULT '[]'::jsonb,
  responses JSONB DEFAULT '[]'::jsonb,
  ai_feedback JSONB DEFAULT '{}'::jsonb,
  overall_score DECIMAL(3,2) DEFAULT 0.00,
  completed_at TIMESTAMPTZ,
  duration_minutes INTEGER,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Interview Question Bank
CREATE TABLE IF NOT EXISTS ai_interview_questions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  question TEXT NOT NULL,
  category TEXT NOT NULL CHECK (category IN ('technical', 'behavioral', 'situational', 'case_study')),
  difficulty TEXT NOT NULL CHECK (difficulty IN ('junior', 'mid', 'senior', 'lead')),
  role_types TEXT[] DEFAULT '{}',
  industries TEXT[] DEFAULT '{}',
  skills_required TEXT[] DEFAULT '{}',
  ideal_answer_keywords TEXT[] DEFAULT '{}',
  scoring_criteria JSONB DEFAULT '{}'::jsonb,
  usage_count INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- AI Career Insights
CREATE TABLE IF NOT EXISTS ai_career_insights (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  insight_type TEXT NOT NULL CHECK (insight_type IN ('skill_gap', 'career_path', 'salary_insights', 'market_trends', 'networking')),
  insight_data JSONB NOT NULL DEFAULT '{}'::jsonb,
  confidence_score DECIMAL(3,2) DEFAULT 0.00,
  action_items JSONB DEFAULT '[]'::jsonb,
  is_read BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- AI Skill Gap Analysis
CREATE TABLE IF NOT EXISTS ai_skill_gap_analyses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  target_role TEXT NOT NULL,
  current_skills TEXT[] DEFAULT '{}',
  required_skills TEXT[] DEFAULT '{}',
  missing_skills TEXT[] DEFAULT '{}',
  skill_levels JSONB DEFAULT '{}'::jsonb,
  learning_resources JSONB DEFAULT '[]'::jsonb,
  estimated_timeline_months INTEGER,
  priority_level TEXT CHECK (priority_level IN ('low', 'medium', 'high', 'critical')),
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- AI Portfolio Enhancements
CREATE TABLE IF NOT EXISTS ai_portfolio_enhancements (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  project_id BIGINT REFERENCES projects(id) ON DELETE CASCADE,
  enhancement_type TEXT NOT NULL CHECK (enhancement_type IN ('description', 'keywords', 'structure', 'visuals', 'impact')),
  original_content TEXT,
  suggested_content TEXT,
  ai_reasoning TEXT,
  improvement_score DECIMAL(3,2) DEFAULT 0.00,
  is_applied BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_ai_resume_templates_category ON ai_resume_templates(category);
CREATE INDEX IF NOT EXISTS idx_user_resume_generations_user_id ON user_resume_generations(user_id);
CREATE INDEX IF NOT EXISTS idx_ai_interview_sessions_user_id ON ai_interview_sessions(user_id);
CREATE INDEX IF NOT EXISTS idx_ai_interview_questions_category ON ai_interview_questions(category);
CREATE INDEX IF NOT EXISTS idx_ai_career_insights_user_id ON ai_career_insights(user_id);
CREATE INDEX IF NOT EXISTS idx_ai_skill_gap_analyses_user_id ON ai_skill_gap_analyses(user_id);
CREATE INDEX IF NOT EXISTS idx_ai_portfolio_enhancements_user_id ON ai_portfolio_enhancements(user_id);

