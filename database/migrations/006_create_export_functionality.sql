-- Portfolio Export Functionality Database Schema

-- Export Templates
CREATE TABLE IF NOT EXISTS export_templates (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  description TEXT NOT NULL,
  export_type TEXT NOT NULL CHECK (export_type IN ('pdf', 'website', 'resume')),
  category TEXT NOT NULL CHECK (category IN ('professional', 'creative', 'technical', 'academic', 'minimal')),
  template_config JSONB NOT NULL DEFAULT '{}'::jsonb,
  styling JSONB NOT NULL DEFAULT '{}'::jsonb,
  preview_image TEXT,
  is_premium BOOLEAN DEFAULT FALSE,
  usage_count INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Export History
CREATE TABLE IF NOT EXISTS export_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  export_type TEXT NOT NULL CHECK (export_type IN ('pdf', 'website', 'resume')),
  template_id BIGINT REFERENCES export_templates(id) ON DELETE SET NULL,
  export_config JSONB NOT NULL DEFAULT '{}'::jsonb,
  file_path TEXT,
  file_size BIGINT,
  download_count INTEGER DEFAULT 0,
  is_public BOOLEAN DEFAULT FALSE,
  expires_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Export Settings
CREATE TABLE IF NOT EXISTS export_settings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  default_pdf_template BIGINT REFERENCES export_templates(id),
  default_website_template BIGINT REFERENCES export_templates(id),
  include_analytics BOOLEAN DEFAULT TRUE,
  watermark_text TEXT,
  custom_branding JSONB DEFAULT '{}'::jsonb,
  privacy_settings JSONB DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id)
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_export_templates_type ON export_templates(export_type);
CREATE INDEX IF NOT EXISTS idx_export_templates_category ON export_templates(category);
CREATE INDEX IF NOT EXISTS idx_export_history_user_id ON export_history(user_id);
CREATE INDEX IF NOT EXISTS idx_export_history_created_at ON export_history(created_at);
CREATE INDEX IF NOT EXISTS idx_export_settings_user_id ON export_settings(user_id);

