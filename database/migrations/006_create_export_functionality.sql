-- Function to increment export template usage
CREATE OR REPLACE FUNCTION increment_export_template_usage(template_id BIGINT)
RETURNS VOID AS $$
BEGIN
  UPDATE export_templates 
  SET usage_count = usage_count + 1, updated_at = NOW()
  WHERE id = template_id;
END;
$$ LANGUAGE plpgsql;

-- Function to get user export statistics
CREATE OR REPLACE FUNCTION get_user_export_stats(user_uuid UUID)
RETURNS TABLE(
  total_exports BIGINT,
  pdf_exports BIGINT,
  website_exports BIGINT,
  resume_exports BIGINT,
  total_downloads BIGINT,
  most_used_template TEXT,
  last_export_date TIMESTAMPTZ
) AS $$
BEGIN
  RETURN QUERY
  WITH 
  export_counts AS (
    SELECT 
      COUNT(*) as total,
      COUNT(*) FILTER (WHERE export_type = 'pdf') as pdf_count,
      COUNT(*) FILTER (WHERE export_type = 'website') as website_count,
      COUNT(*) FILTER (WHERE export_type = 'resume') as resume_count,
      SUM(download_count) as downloads,
      MAX(created_at) as last_date
    FROM export_history 
    WHERE user_id = user_uuid
  ),
  template_usage AS (
    SELECT 
      et.name,
      COUNT(*) as usage_count
    FROM export_history eh
    JOIN export_templates et ON eh.template_id = et.id
    WHERE eh.user_id = user_uuid
    GROUP BY et.id, et.name
    ORDER BY usage_count DESC
    LIMIT 1
  )
  SELECT 
    ec.total,
    ec.pdf_count,
    ec.website_count,
    ec.resume_count,
    COALESCE(ec.downloads, 0),
    COALESCE(tu.name, 'No template used'),
    ec.last_date
  FROM export_counts ec
  LEFT JOIN template_usage tu ON true;
END;
$$ LANGUAGE plpgsql;

-- Function to cleanup expired exports
CREATE OR REPLACE FUNCTION cleanup_expired_exports()
RETURNS INTEGER AS $$
DECLARE
  deleted_count INTEGER;
BEGIN
  DELETE FROM export_history 
  WHERE expires_at IS NOT NULL 
    AND expires_at < NOW();
  
  GET DIAGNOSTICS deleted_count = ROW_COUNT;
  
  RETURN deleted_count;
END;
$$ LANGUAGE plpgsql;
