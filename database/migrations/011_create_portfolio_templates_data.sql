-- Create user template selections table
CREATE TABLE IF NOT EXISTS user_template_selections (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  template_id BIGINT NOT NULL REFERENCES portfolio_templates(id) ON DELETE CASCADE,
  customizations JSONB DEFAULT '{}'::jsonb,
  applied_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id)
);

-- Create index for user template lookups
CREATE INDEX IF NOT EXISTS idx_user_template_selections_user_id ON user_template_selections(user_id);

-- Function to increment template usage count
CREATE OR REPLACE FUNCTION increment_template_usage(template_id BIGINT)
RETURNS VOID AS $$
BEGIN
  UPDATE portfolio_templates 
  SET usage_count = usage_count + 1, updated_at = NOW()
  WHERE id = template_id;
END;
$$ LANGUAGE plpgsql;

-- Enable RLS for templates
ALTER TABLE portfolio_templates ENABLE ROW LEVEL SECURITY;

-- Everyone can view templates
CREATE POLICY "Anyone can view templates" ON portfolio_templates
  FOR SELECT USING (true);

-- Enable RLS for user template selections
ALTER TABLE user_template_selections ENABLE ROW LEVEL SECURITY;

-- Users can view their own template selections
CREATE POLICY "Users can view own template selections" ON user_template_selections
  FOR SELECT USING (auth.uid() = user_id);

-- Users can insert their own template selections
CREATE POLICY "Users can insert own template selections" ON user_template_selections
  FOR INSERT WITH CHECK (auth.uid() = user_id);

-- Users can update their own template selections
CREATE POLICY "Users can update own template selections" ON user_template_selections
  FOR UPDATE USING (auth.uid() = user_id);

-- Users can delete their own template selections
CREATE POLICY "Users can delete own template selections" ON user_template_selections
  FOR DELETE USING (auth.uid() = user_id);

