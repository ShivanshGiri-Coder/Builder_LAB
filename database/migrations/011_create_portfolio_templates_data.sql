-- Create portfolio templates table
CREATE TABLE IF NOT EXISTS portfolio_templates (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  description TEXT NOT NULL,
  category TEXT NOT NULL CHECK (category IN ('developer', 'designer', 'data_scientist', 'student', 'general')),
  preview_image TEXT,
  color_scheme JSONB DEFAULT '{"primary": "#8b5cf6", "secondary": "#6366f1", "accent": "#ec4899"}'::jsonb,
  layout_config JSONB NOT NULL DEFAULT '{}'::jsonb,
  sections JSONB NOT NULL DEFAULT '["hero", "about", "skills", "projects", "contact"]'::jsonb,
  is_premium BOOLEAN DEFAULT FALSE,
  usage_count INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create index for category filtering
CREATE INDEX IF NOT EXISTS idx_portfolio_templates_category ON portfolio_templates(category);

-- Create index for premium filtering
CREATE INDEX IF NOT EXISTS idx_portfolio_templates_is_premium ON portfolio_templates(is_premium);

