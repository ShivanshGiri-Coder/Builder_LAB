-- Function to apply theme to user profile
CREATE OR REPLACE FUNCTION apply_theme_to_profile(
  user_uuid UUID,
  theme_id BIGINT,
  custom_config JSONB DEFAULT NULL
) RETURNS BOOLEAN AS $$
DECLARE
  theme_record RECORD;
  existing_customization RECORD;
BEGIN
  -- Get theme details
  SELECT * INTO theme_record FROM portfolio_themes WHERE id = theme_id;
  
  IF NOT FOUND THEN
    RETURN FALSE;
  END IF;
  
  -- Check if user has existing customization
  SELECT * INTO existing_customization 
  FROM user_theme_customizations 
  WHERE user_id = user_uuid AND theme_id = theme_id;
  
  IF existing_customization.id IS NOT NULL THEN
    -- Update existing customization
    UPDATE user_theme_customizations 
    SET 
      custom_config = COALESCE(custom_config, theme_record.theme_config),
      is_active = TRUE,
      updated_at = NOW()
    WHERE user_id = user_uuid AND theme_id = theme_id;
  ELSE
    -- Create new customization
    INSERT INTO user_theme_customizations (user_id, theme_id, custom_config, is_active)
    VALUES (user_uuid, theme_id, COALESCE(custom_config, theme_record.theme_config), TRUE);
  END IF;
  
  -- Deactivate other themes for this user
  UPDATE user_theme_customizations 
  SET is_active = FALSE 
  WHERE user_id = user_uuid AND theme_id != theme_id;
  
  -- Record theme usage
  INSERT INTO theme_usage_history (user_id, theme_id)
  VALUES (user_uuid, theme_id);
  
  -- Update theme usage count
  UPDATE portfolio_themes 
  SET usage_count = usage_count + 1 
  WHERE id = theme_id;
  
  RETURN TRUE;
END;
$$ LANGUAGE plpgsql;

-- Function to get user's active theme
CREATE OR REPLACE FUNCTION get_user_active_theme(user_uuid UUID)
RETURNS TABLE(
  theme_id BIGINT,
  theme_name TEXT,
  theme_category TEXT,
  theme_config JSONB,
  custom_config JSONB,
  applied_at TIMESTAMPTZ
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    pt.id,
    pt.name,
    pt.category,
    pt.theme_config,
    utc.custom_config,
    utc.updated_at
  FROM user_theme_customizations utc
  JOIN portfolio_themes pt ON utc.theme_id = pt.id
  WHERE utc.user_id = user_uuid AND utc.is_active = TRUE;
END;
$$ LANGUAGE plpgsql;

-- Function to generate theme CSS
CREATE OR REPLACE FUNCTION generate_theme_css(theme_config JSONB)
RETURNS TEXT AS $$
DECLARE
  css_content TEXT := '';
  colors JSONB;
  typography JSONB;
  layout JSONB;
  animations JSONB;
BEGIN
  -- Extract theme components
  colors := COALESCE(theme_config->'colors', '{}'::jsonb);
  typography := COALESCE(theme_config->'typography', '{}'::jsonb);
  layout := COALESCE(theme_config->'layout', '{}'::jsonb);
  animations := COALESCE(theme_config->'animations', '{}'::jsonb);
  
  -- Generate CSS variables
  css_content := css_content || ':root {' || E'\n';
  
  -- Color variables
  css_content := css_content || '  --color-primary: ' || COALESCE(colors->>'primary', '#3b82f6') || ';' || E'\n';
  css_content := css_content || '  --color-secondary: ' || COALESCE(colors->>'secondary', '#6b7280') || ';' || E'\n';
  css_content := css_content || '  --color-accent: ' || COALESCE(colors->>'accent', '#60a5fa') || ';' || E'\n';
  css_content := css_content || '  --color-background: ' || COALESCE(colors->>'background', '#ffffff') || ';' || E'\n';
  css_content := css_content || '  --color-text: ' || COALESCE(colors->>'text', '#1f2937') || ';' || E'\n';
  css_content := css_content || '  --color-text-secondary: ' || COALESCE(colors->>'textSecondary', '#6b7280') || ';' || E'\n';
  
  -- Typography variables
  css_content := css_content || '  --font-family: ' || COALESCE(typography->>'fontFamily', 'Inter') || ';' || E'\n';
  css_content := css_content || '  --font-heading: ' || COALESCE(typography->>'headingFont', 'Inter SemiBold') || ';' || E'\n';
  css_content := css_content || '  --font-size-body: ' || COALESCE(typography->>'bodySize', '16px') || ';' || E'\n';
  css_content := css_content || '  --font-size-heading: ' || COALESCE(typography->>'headingSize', '2.5rem') || ';' || E'\n';
  
  -- Layout variables
  css_content := css_content || '  --container-width: ' || COALESCE(layout->>'containerWidth', '1200px') || ';' || E'\n';
  css_content := css_content || '  --section-spacing: ' || COALESCE(layout->>'sectionSpacing', '80px') || ';' || E'\n';
  css_content := css_content || '  --border-radius-card: ' || COALESCE(layout->>'cardRadius', '12px') || ';' || E'\n';
  css_content := css_content || '  --border-radius-button: ' || COALESCE(layout->>'buttonRadius', '8px') || ';' || E'\n';
  
  -- Animation variables
  css_content := css_content || '  --animation-duration: ' || COALESCE(animations->>'duration', '0.3s') || ';' || E'\n';
  css_content := css_content || '  --animation-easing: ' || COALESCE(animations->>'easing', 'ease-in-out') || ';' || E'\n';
  
  css_content := css_content || '}' || E'\n';
  
  -- Generate utility classes
  css_content := css_content || E'\n';
  css_content := css_content || '/* Theme Utility Classes */' || E'\n';
  css_content := css_content || '.bg-primary { background-color: var(--color-primary); }' || E'\n';
  css_content := css_content || '.bg-secondary { background-color: var(--color-secondary); }' || E'\n';
  css_content := css_content || '.bg-accent { background-color: var(--color-accent); }' || E'\n';
  css_content := css_content || '.text-primary { color: var(--color-primary); }' || E'\n';
  css_content := css_content || '.text-secondary { color: var(--color-secondary); }' || E'\n';
  css_content := css_content || '.text-accent { color: var(--color-accent); }' || E'\n';
  
  css_content := css_content || E'\n';
  css_content := css_content || '/* Typography Classes */' || E'\n';
  css_content := css_content || 'body { font-family: var(--font-family); font-size: var(--font-size-body); color: var(--color-text); }' || E'\n';
  css_content := css_content || 'h1, h2, h3, h4, h5, h6 { font-family: var(--font-heading); color: var(--color-text); }' || E'\n';
  css_content := css_content || 'h1 { font-size: var(--font-size-heading); }' || E'\n';
  
  css_content := css_content || E'\n';
  css_content := css_content || '/* Layout Classes */' || E'\n';
  css_content := css_content || '.container { max-width: var(--container-width); margin: 0 auto; padding: 0 20px; }' || E'\n';
  css_content := css_content || '.card { border-radius: var(--border-radius-card); }' || E'\n';
  css_content := css_content || '.btn { border-radius: var(--border-radius-button); }' || E'\n';
  
  css_content := css_content || E'\n';
  css_content := css_content || '/* Animation Classes */' || E'\n';
  css_content := css_content || '.transition { transition: all var(--animation-duration) var(--animation-easing); }' || E'\n';
  
  RETURN css_content;
END;
$$ LANGUAGE plpgsql;

-- Function to update theme rating
CREATE OR REPLACE FUNCTION update_theme_rating(theme_id BIGINT)
RETURNS VOID AS $$
DECLARE
  avg_rating DECIMAL;
  review_count_val INTEGER;
BEGIN
  SELECT 
    COALESCE(AVG(rating), 0),
    COUNT(*)
  INTO avg_rating, review_count_val
  FROM theme_reviews 
  WHERE theme_id = theme_id;
  
  UPDATE portfolio_themes 
  SET 
    rating = avg_rating,
    review_count = review_count_val,
    updated_at = NOW()
  WHERE id = theme_id;
END;
$$ LANGUAGE plpgsql;

-- Function to get popular themes
CREATE OR REPLACE FUNCTION get_popular_themes(limit_count INTEGER DEFAULT 10)
RETURNS TABLE(
  theme_id BIGINT,
  theme_name TEXT,
  theme_category TEXT,
  rating DECIMAL,
  download_count INTEGER,
  usage_count INTEGER,
  preview_image TEXT
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    pt.id,
    pt.name,
    pt.category,
    pt.rating,
    pt.download_count,
    pt.usage_count,
    pt.preview_image
  FROM portfolio_themes pt
  WHERE pt.is_public = TRUE
  ORDER BY 
    (pt.rating * 0.4 + pt.download_count * 0.3 + pt.usage_count * 0.3) DESC,
    pt.created_at DESC
  LIMIT limit_count;
END;
$$ LANGUAGE plpgsql;
