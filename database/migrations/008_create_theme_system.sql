-- Portfolio Theme System Database Schema

-- Theme Definitions
CREATE TABLE IF NOT EXISTS portfolio_themes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  description TEXT NOT NULL,
  category TEXT NOT NULL CHECK (category IN ('professional', 'creative', 'minimal', 'dark', 'vibrant', 'custom')),
  author_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  is_public BOOLEAN DEFAULT FALSE,
  is_premium BOOLEAN DEFAULT FALSE,
  price DECIMAL(10,2) DEFAULT 0.00,
  rating DECIMAL(3,2) DEFAULT 0.00,
  review_count INTEGER DEFAULT 0,
  download_count INTEGER DEFAULT 0,
  usage_count INTEGER DEFAULT 0,
  theme_config JSONB NOT NULL DEFAULT '{}'::jsonb,
  preview_image TEXT,
  tags TEXT[] DEFAULT '{}',
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- User Theme Customizations
CREATE TABLE IF NOT EXISTS user_theme_customizations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  theme_id BIGINT REFERENCES portfolio_themes(id) ON DELETE CASCADE,
  custom_name TEXT,
  custom_config JSONB NOT NULL DEFAULT '{}'::jsonb,
  is_active BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id, theme_id)
);

-- Theme Usage History
CREATE TABLE IF NOT EXISTS theme_usage_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  theme_id BIGINT REFERENCES portfolio_themes(id) ON DELETE SET NULL,
  applied_at TIMESTAMPTZ DEFAULT NOW(),
  session_duration INTEGER, -- in minutes
  user_agent TEXT,
  ip_address INET
);

-- Theme Reviews and Ratings
CREATE TABLE IF NOT EXISTS theme_reviews (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  theme_id BIGINT REFERENCES portfolio_themes(id) ON DELETE CASCADE,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  rating INTEGER CHECK (rating >= 1 AND rating <= 5),
  review_text TEXT,
  is_verified_purchase BOOLEAN DEFAULT FALSE,
  helpful_count INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(theme_id, user_id)
);

-- Theme Marketplace Categories
CREATE TABLE IF NOT EXISTS theme_categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  description TEXT,
  icon TEXT,
  sort_order INTEGER DEFAULT 0,
  is_active BOOLEAN DEFAULT TRUE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Theme Tags
CREATE TABLE IF NOT EXISTS theme_tags (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  color TEXT DEFAULT '#6b7280',
  usage_count INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Theme-Tag Relationships
CREATE TABLE IF NOT EXISTS theme_tag_relations (
  theme_id BIGINT REFERENCES portfolio_themes(id) ON DELETE CASCADE,
  tag_id BIGINT REFERENCES theme_tags(id) ON DELETE CASCADE,
  PRIMARY KEY (theme_id, tag_id)
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_portfolio_themes_category ON portfolio_themes(category);
CREATE INDEX IF NOT EXISTS idx_portfolio_themes_author_id ON portfolio_themes(author_id);
CREATE INDEX IF NOT EXISTS idx_portfolio_themes_is_public ON portfolio_themes(is_public);
CREATE INDEX IF NOT EXISTS idx_portfolio_themes_rating ON portfolio_themes(rating DESC);
CREATE INDEX IF NOT EXISTS idx_portfolio_themes_download_count ON portfolio_themes(download_count DESC);
CREATE INDEX IF NOT EXISTS idx_user_theme_customizations_user_id ON user_theme_customizations(user_id);
CREATE INDEX IF NOT EXISTS idx_user_theme_customizations_is_active ON user_theme_customizations(is_active);
CREATE INDEX IF NOT EXISTS idx_theme_usage_history_user_id ON theme_usage_history(user_id);
CREATE INDEX IF NOT EXISTS idx_theme_usage_history_applied_at ON theme_usage_history(applied_at DESC);
CREATE INDEX IF NOT EXISTS idx_theme_reviews_theme_id ON theme_reviews(theme_id);
CREATE INDEX IF NOT EXISTS idx_theme_reviews_rating ON theme_reviews(rating DESC);

