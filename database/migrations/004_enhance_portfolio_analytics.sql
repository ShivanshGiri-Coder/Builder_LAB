ALTER TABLE portfolio_analytics ADD COLUMN IF NOT EXISTS referrer TEXT;
ALTER TABLE portfolio_analytics ADD COLUMN IF NOT EXISTS country TEXT;
ALTER TABLE portfolio_analytics ADD COLUMN IF NOT EXISTS city TEXT;
