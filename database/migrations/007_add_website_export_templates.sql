CREATE OR REPLACE FUNCTION optimize_website_assets(export_id BIGINT)
RETURNS TABLE(
  asset_id BIGINT,
  original_size BIGINT,
  optimized_size BIGINT,
  compression_ratio DECIMAL
) AS $$
BEGIN
