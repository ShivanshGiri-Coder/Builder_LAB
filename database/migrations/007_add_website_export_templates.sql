    'title', COALESCE(user_profile_data->>'full_name', 'Portfolio') || ' - Professional Portfolio',
    'description', COALESCE(user_profile_data->>'bio', 'Professional portfolio showcasing projects and skills'),
    'keywords', COALESCE(ARRAY_TO_STRING(ARRAY(SELECT jsonb_array_elements_text(user_profile_data->'skills')), ', '), 'portfolio, projects, skills'),
    'author', COALESCE(user_profile_data->>'full_name', 'Portfolio Owner'),
    'og_type', 'website',
    'og_site_name', 'Builder LAB Portfolio',
    'twitter_card', 'summary_large_image'
  );
END;
$$ LANGUAGE plpgsql;
