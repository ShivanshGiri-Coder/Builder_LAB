-- Create website export assets table
CREATE TABLE IF NOT EXISTS website_export_assets (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  export_id BIGINT REFERENCES export_history(id) ON DELETE CASCADE,
  asset_type TEXT NOT NULL CHECK (asset_type IN ('css', 'js', 'image', 'font', 'icon')),
  file_path TEXT NOT NULL,
  file_name TEXT NOT NULL,
  file_size BIGINT,
  mime_type TEXT,
  optimized_path TEXT,
  is_compressed BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create website export configurations table
CREATE TABLE IF NOT EXISTS website_export_configs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  template_id BIGINT REFERENCES export_templates(id) ON DELETE SET NULL,
  config_name TEXT NOT NULL,
  seo_settings JSONB DEFAULT '{}'::jsonb,
  analytics_settings JSONB DEFAULT '{}'::jsonb,
  performance_settings JSONB DEFAULT '{}'::jsonb,
  custom_css TEXT,
  custom_js TEXT,
  is_default BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_website_export_assets_export_id ON website_export_assets(export_id);
CREATE INDEX IF NOT EXISTS idx_website_export_assets_type ON website_export_assets(asset_type);
CREATE INDEX IF NOT EXISTS idx_website_export_configs_user_id ON website_export_configs(user_id);
CREATE INDEX IF NOT EXISTS idx_website_export_configs_template_id ON website_export_configs(template_id);

