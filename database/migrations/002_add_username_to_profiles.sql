-- Add username field to profiles table
ALTER TABLE profiles ADD COLUMN username TEXT UNIQUE;

-- Create index for username for faster lookups
CREATE INDEX IF NOT EXISTS idx_profiles_username ON profiles(username);

-- Create function to generate username from full_name if not provided
CREATE OR REPLACE FUNCTION generate_username(full_name TEXT)
RETURNS TEXT AS $$
BEGIN
  -- Remove special characters and convert to lowercase
  RETURN lower(regexp_replace(full_name, '[^a-zA-Z0-9\s]', '', 'g'));
END;
$$ LANGUAGE plpgsql;

