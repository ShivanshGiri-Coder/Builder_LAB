-- Create portfolio analytics table for tracking views
CREATE TABLE IF NOT EXISTS portfolio_analytics (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username TEXT NOT NULL REFERENCES profiles(username) ON DELETE CASCADE,
  view_count INTEGER DEFAULT 1,
  last_viewed_at TIMESTAMPTZ DEFAULT NOW(),
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create index for analytics
CREATE INDEX IF NOT EXISTS idx_portfolio_analytics_username ON portfolio_analytics(username);

-- Function to increment portfolio view count
CREATE OR REPLACE FUNCTION increment_portfolio_view(portfolio_username TEXT)
RETURNS VOID AS $$
BEGIN
  INSERT INTO portfolio_analytics (username, view_count, last_viewed_at)
  VALUES (portfolio_username, 1, NOW())
  ON CONFLICT (username) 
  DO UPDATE SET 
    view_count = portfolio_analytics.view_count + 1,
    last_viewed_at = NOW();
END;
$$ LANGUAGE plpgsql;
